const primeiroValor = 10
const segundoValor = 5

console.log("Soma =", primeiroValor + segundoValor)
console.log("Subtração =", primeiroValor - segundoValor)
console.log("Divisão =", primeiroValor / segundoValor)
console.log("Multiplicação =", primeiroValor * segundoValor)
console.log("Resto da divisão =", primeiroValor % segundoValor)

/* Quando usa o operador de soma com tipos de variáveis diferentes 
ao invés de somar os valor é feito a CONCATENAÇÃO */